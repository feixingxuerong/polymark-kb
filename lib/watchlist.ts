import fs from 'fs'
import path from 'path'

const OUTPUTS_DIR = path.join(process.cwd(), 'content/poly-knowledge/outputs')

// Extended watchlist item with optional fields
export interface WatchlistItem {
  question: string
  score: number
  spread_pct: number | null
  liquidity: number | null
  days_to_event: number | null
  reason: string
  market_id?: string
  url?: string
  slug?: string
  endDate?: string
  end_date?: string
  negRisk?: boolean
  weather_signal_score?: number
  // Extended fields from task pipeline
  category?: string
  action?: string
  entry_plan?: string
  thesis?: string
  monitor_sources?: string[]
  key_risks?: string[]
  // Probability calculation fields (generated by task #40)
  assistant_prob?: number | null
  assistant_evidence?: {
    threshold?: number | null
    predicted_max?: number | null
    sigma?: number | null
    station?: string | null
    updated_at?: string | null
    market_price?: number | null
  }
}

export interface WatchlistData {
  date?: string
  generatedAt?: string
  generated_at?: string
  markets?: WatchlistItem[]
  watchlist?: (WatchlistItem & { rank?: number })[]
}

