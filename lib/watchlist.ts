// Check if action is "consider" or "tracking"
export function isActionable(item: WatchlistItem): boolean {
  if (!item.action) return false
  const action = item.action.toLowerCase()
  return action.includes('考虑') || action.includes('跟踪') || action.includes('tracking') || action.includes('consider')
}

function normalizeWatchlistData(date: string, data: WatchlistData): { date: string; generatedAt: string; items: WatchlistItem[] } {
  const generatedAt = data.generatedAt || data.generated_at || new Date().toISOString()
  const items = (data.markets || data.watchlist || []).map((it: any) => {
    const scoreVal =
      typeof it.score === 'number'
        ? it.score
        : typeof it.scores?.total === 'number'
          ? it.scores.total
          : Number(it.score ?? 0)

    const spreadVal = typeof it.spread_pct === 'number' ? it.spread_pct : it.spread_pct == null ? null : Number(it.spread_pct)
    const liqVal = typeof it.liquidity === 'number' ? it.liquidity : it.liquidity == null ? null : Number(it.liquidity)
    const daysVal = typeof it.days_to_event === 'number' ? it.days_to_event : it.days_to_event == null ? null : Number(it.days_to_event)

    return {
      ...it,
      score: Number.isFinite(scoreVal) ? scoreVal : 0,
      spread_pct: Number.isFinite(spreadVal as any) ? (spreadVal as number) : null,
      liquidity: Number.isFinite(liqVal as any) ? (liqVal as number) : null,
      days_to_event: Number.isFinite(daysVal as any) ? (daysVal as number) : null,
      reason: it.reason || '',
      category: it.category,
      action: it.action,
      entry_plan: it.entry_plan,
      thesis: it.thesis,
      monitor_sources: it.monitor_sources,
      key_risks: it.key_risks,
      slug: it.slug,
      endDate: it.endDate || it.end_date,
      negRisk: it.negRisk,
      weather_signal_score: it.weather_signal_score,
      // Probability calculation fields (generated by task #40)
      assistant_prob: it.assistant_prob ?? null,
      assistant_evidence: it.assistant_evidence ? {
        threshold: it.assistant_evidence.threshold ?? null,
        predicted_max: it.assistant_evidence.predicted_max ?? null,
        sigma: it.assistant_evidence.sigma ?? null,
        station: it.assistant_evidence.station ?? null,
        updated_at: it.assistant_evidence.updated_at ?? null,
        market_price: it.assistant_evidence.market_price ?? null,
      } : undefined,
    } as WatchlistItem
  })
  return { date, generatedAt, items }
}

/**
 * Get all watchlist files sorted by date (newest first).
 */
export function getAllWatchlistFiles(): string[] {
  if (!fs.existsSync(OUTPUTS_DIR)) {
    return []
  }

  const files = fs.readdirSync(OUTPUTS_DIR)
  const watchlistFiles = files
    .filter((f) => f.match(/^watchlist-\d{4}-\d{2}-\d{2}\.json$/))
    .map((f) => path.join(OUTPUTS_DIR, f))
    .sort()
    .reverse()

  return watchlistFiles
}

/**
 * Get the latest watchlist file path.
 */
export function getLatestWatchlistPath(): string | null {
  const files = getAllWatchlistFiles()
  return files.length > 0 ? files[0] : null
}

/**
 * Get the latest watchlist date string (YYYY-MM-DD).
 */
export function getLatestWatchlistDate(): string | null {
  const latestPath = getLatestWatchlistPath()
  if (!latestPath) return null

  const match = path.basename(latestPath).match(/watchlist-(\d{4}-\d{2}-\d{2})\.json/)
  return match ? match[1] : null
}

/**
 * Read and parse a watchlist file by date.
 */
export function getWatchlistByDate(date: string): { date: string; generatedAt: string; items: WatchlistItem[] } | null {
  const filePath = path.join(OUTPUTS_DIR, `watchlist-${date}.json`)

  if (!fs.existsSync(filePath)) return null

  try {
    const raw = fs.readFileSync(filePath, 'utf-8')
    const data = JSON.parse(raw) as WatchlistData
    return normalizeWatchlistData(date, data)
  } catch (error) {
    console.error(`Error parsing watchlist ${date}:`, error)
    return null
  }
}

/**
 * Get the latest watchlist data.
 */
export function getLatestWatchlist(): { date: string; generatedAt: string; items: WatchlistItem[] } | null {
  const latestDate = getLatestWatchlistDate()
  if (!latestDate) return null
  return getWatchlistByDate(latestDate)
}

/**
 * Get watchlist items with optional limit.
 */
export function getWatchlistItems(date: string, limit?: number): WatchlistItem[] {
  const data = getWatchlistByDate(date)
  if (!data) return []

  const items = [...data.items].sort((a, b) => b.score - a.score)
  return limit ? items.slice(0, limit) : items
}

